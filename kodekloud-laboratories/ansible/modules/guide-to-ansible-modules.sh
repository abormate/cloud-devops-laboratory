# Ansible are categorized into various groups based on their functionality.

# module category -- System --> actions to performed on a system level
# user, group, hostname, iptables, lvg, lvol, make, mount, ping, timezone, systemd, service

# module category -- Commands --> used to execute commands or scripts on a host
# command, expect, raw, script, shell

# module category -- Files --> used to work with files
# acl, archive, copy, file, find, lineinfile, replace, stat, template, unarchive

# module category -- Database --> used to work with databases
# mongodb, mssql, mysql, postgresql, proxysql, vertica

# module category -- Cloud --> used for cloud vendors 
# amazon, atomic, azure, centrylink, cloudscale, cloudstack, digital ocean, docker, google, linode, openstack, rackspace, smartos, softlayer, VMware

# module category -- Windows --> help you ansible in a windows environment
# win_copy, win_command, win_domain, win_iis_website, win_msg, win_msi, win_package, win_ping, win_path, win_robocopy, win_regedit, win_shell, win_service, win_user


